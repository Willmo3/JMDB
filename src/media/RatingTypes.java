package media;

/**
 * The ratings that can be received by a query for movie ratings.
 * 
 * @author Matthew Potter
 * @version 12/08/2022
 */
public enum RatingTypes
{
  /**
   * Rating by IMDb.
   */
  IMDB,
  
  /**
   * Rating by Metacritic.
   */
  METACRITIC,
  
  /**
   * Rating by The Movie Database.
   */
  MOVIEDB,
  
  
  /**
   * Rating by Rotten Tomatoes.
   */
  ROTTEN_TOMATOES,
  
  /**
   * Rating by Film Affinity.
   */
  FILM_AFFINITY;
}
